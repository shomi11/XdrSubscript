import Foundation
import UIKit
import SwiftUI

extension Color {
    
    static var systemBackgroundColor: Color {
        return Color(uiColor: UIColor.systemBackground)
    }
    
    static var secondaryBackgroundColor: Color {
        return Color(uiColor: UIColor.secondarySystemBackground)
    }
}
